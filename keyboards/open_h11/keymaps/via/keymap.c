void render_layer_status(void) {
    int layer_idx = 0;
    switch (get_highest_layer(layer_state)) {
        case _DJMAX:
            layer_idx = 0;
            break;
        case _RIFT_DANCER:
            layer_idx = 1;
            break;
        case _RHYTHM_DOCTOR:
            layer_idx = 2;
            break;
        case _4:
            layer_idx = 3;
            break;
        default:
            layer_idx = 2;
            break;
    }

    oled_write_raw_P(layer_status[layer_idx], sizeof(layer_status[0]));
}
